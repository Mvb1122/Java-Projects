// Vocab:
  // public
    // Variables, methods, or classes which are availible outside the class.
  // private
    // Variables, methods, or classes which are only availible inside the class.
  // instance variable
    // Variables which are assigned on an instance-wise basis; they are different
    // for each instance (or object) of a class.
  // attribute
    // A characteristic of an object.
  // has-a relationship
    // The relationship between an object and its instance variables.
  // data encapsulation
    // An object-oriented programming conceppt where the details of a class are hidden from the user, it's also refered to as information hiding.